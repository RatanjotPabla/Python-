#Purpose:           This program will output the first twenty powers of five with the use of a for loop.
#-----------------------------------------------------------------------------------------------------------------------------------------------------------------

for count in range(20): #The count will be in range of 20. This means that the count will start at zero and the range is from 0-20.
    count+= 1   #The counter which will add one to each time the loop occurs in the range of 20 times.
    print(5**count) #The output will be 5 to the exponent 'count'. Each line will print 5 to the next exponent each time starting from 5**1 and ending to 5**20.
